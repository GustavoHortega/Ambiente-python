#SOMA DOS CINCO PRIMEIROS NÚMEROS INTEIROS E POSITIVOS
print( 1 + 2 + 3 + 4 + 5 )

#MÉDIA DE 23,19 E 31
print( (23 + 19 + 31) / 3 )

#NÚMERO DE VEZES QUE 73 CABE EM 403
print(403 // 73) # "//" divisão somente da parte inteira

#SOBRA DA DIVISÃO ACIMA
print(403 % 73)

#DOIS ELEVADO A DÉCIMA POTÊNCIA
print( 2 ** 10)

#VALOR ABSOLUTO DA DIFERENCÇA ENTRE 54 E 57 (use a instrução "abs")
print(abs(54 - 57))

#MENOR VALOE ENTRE 34,29 E 31 (use a instrução "min")
print(min(34, 29, 31))